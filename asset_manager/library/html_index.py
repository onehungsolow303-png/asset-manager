"""HTML catalog index generator.

Walks the asset catalog and writes a single self-contained HTML file
that shows every asset with a thumbnail, kind, source, license, and
provenance metadata. The file lives at `<baked_root>/index.html` and
is regenerated by Asset Manager whenever the catalog changes.

Why this exists:
  When the user is remote and we generate or import a batch of assets,
  they need a way to audit them quickly without booting Unity. The
  HTML index renders in any browser, supports filtering by kind/source/
  license via simple JS, and shows the actual images so visual review
  takes seconds instead of minutes.

Output format:
  - Single HTML file, no external dependencies
  - Inline CSS for layout (responsive grid)
  - Tiny inline JS for filter controls
  - Image thumbnails are direct file:// references to the asset paths
    (no base64 encoding — keeps the HTML small and the images load
    on demand from disk)
  - Non-image assets (.glb, .fbx) get a placeholder card with metadata
    only — Blender thumbnail rendering is a future enhancement

Limits:
  - file:// links work when opening the HTML directly from disk on the
    same machine that generated it. They will NOT work if the HTML is
    served over HTTP without copying or symlinking the assets. For
    remote review we'd need to add a /thumbnails HTTP endpoint, which
    is a future enhancement.
"""
from __future__ import annotations

import html
import logging
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)

# Image extensions we can show inline. Others get a metadata-only card.
_IMAGE_EXTENSIONS = {".png", ".jpg", ".jpeg", ".gif", ".webp"}


def regenerate_index(
    catalog: Any,
    out_path: Path,
) -> int:
    """Build the HTML index from the current catalog state.

    catalog must implement .all() returning a list of manifest dicts.
    out_path is where the HTML file should be written; the parent
    directory is created if it doesn't exist.

    Returns the number of assets included in the index.
    """
    assets = catalog.all() if hasattr(catalog, "all") else []
    out_path.parent.mkdir(parents=True, exist_ok=True)

    cards_html = "\n".join(_render_card(a) for a in assets)
    page = _PAGE_TEMPLATE.format(
        count=len(assets),
        cards=cards_html,
    )

    out_path.write_text(page, encoding="utf-8")
    logger.info("[html_index] wrote %d assets to %s", len(assets), out_path)
    return len(assets)


def _render_card(asset: dict[str, Any]) -> str:
    """Render a single asset as a flex card."""
    asset_id = html.escape(str(asset.get("asset_id", "?")))
    kind = html.escape(str(asset.get("kind", "?")))
    source = html.escape(str(asset.get("source", "unknown")))
    license_code = html.escape(str(asset.get("license", "unknown")))
    pack_name = html.escape(str(asset.get("pack_name") or "—"))
    cost_usd = float(asset.get("cost_usd") or 0.0)
    swap_safe = bool(asset.get("swap_safe", True))
    redistribution = bool(asset.get("redistribution", True))
    path = str(asset.get("path", ""))
    biome = html.escape(str(asset.get("biome") or "—"))
    tags = asset.get("tags") or []
    tags_str = html.escape(", ".join(str(t) for t in tags) or "—")

    # Source colorization helps the eye scan a big batch
    source_class = f"source-{source}"
    redist_badge = "✓" if redistribution else "✗"
    swap_badge = "swap-safe" if swap_safe else "curated"

    # Image preview if it's a recognized image extension; otherwise
    # show a placeholder card with the file extension prominent
    suffix = Path(path).suffix.lower() if path else ""
    if suffix in _IMAGE_EXTENSIONS:
        # file:// URI works when opening the index from disk locally
        file_uri = f"file:///{path.replace(chr(92), '/').lstrip('/')}"
        preview = f'<img src="{html.escape(file_uri)}" alt="{asset_id}" loading="lazy">'
    else:
        ext_badge = (suffix or "?").lstrip(".").upper() or "?"
        preview = f'<div class="ext-placeholder">{html.escape(ext_badge)}</div>'

    return f"""
    <div class="card" data-kind="{kind}" data-source="{source}" data-license="{license_code}">
      <div class="preview">{preview}</div>
      <div class="meta">
        <div class="title">{asset_id}</div>
        <div class="row"><span class="label">kind:</span> {kind}</div>
        <div class="row"><span class="label">source:</span> <span class="{source_class}">{source}</span></div>
        <div class="row"><span class="label">pack:</span> {pack_name}</div>
        <div class="row"><span class="label">license:</span> {license_code} <span class="redist">{redist_badge}</span></div>
        <div class="row"><span class="label">cost:</span> ${cost_usd:.4f}</div>
        <div class="row"><span class="label">biome:</span> {biome}</div>
        <div class="row"><span class="label">tags:</span> {tags_str}</div>
        <div class="row"><span class="label">{swap_badge}</span></div>
      </div>
    </div>
    """


