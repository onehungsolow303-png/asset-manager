"""Asset manifest helpers — defines the canonical shape of every entry
in the catalog and provides helpers for constructing + migrating them.

Schema fields:

  Required (every manifest must have):
    schema_version  Always "1.0.0" for the current contract.
    asset_id        Stable, semantic identifier (NOT a hash). Used as the
                    primary key in the catalog and the file stem on disk.
                    Examples: "wolf", "garth_portrait", "dungeon_wall_corner_01".
    kind            One of the registered asset kinds (creature_token,
                    item_icon, dungeon_tile, terrain, tileset, character,
                    portrait, ...). Used by /select to filter candidates.
    path            Absolute filesystem path to the asset file.

  Catalog metadata (optional but encouraged):
    biome           Theme biome the asset belongs to (forest/ruins/etc).
                    Used by /select scoring.
    tags            List of free-form tags. Used by /select set-overlap match.

  Provenance + cost tracking (added by the asset pipeline rework):
    source          Where the asset came from. One of:
                      "pack"        — imported from a third-party asset pack
                      "procedural"  — generated by Pillow/perlin code
                      "ai_2d"       — generated by a 2D AI gateway (Scenario.gg, SD)
                      "ai_3d"       — generated by a 3D AI gateway (Tripo, Meshy, Rodin)
                      "blender"     — pre-rendered from a 3D model via headless Blender
                      "unknown"     — legacy entry, source not recorded
    pack_name       For source=pack, the human-readable pack identifier
                    (e.g. "KayKit Dungeon Pack Remastered"). None otherwise.
    license         License code from the LICENSES.md table. Examples:
                    "CC0", "KayKit_free", "Quaternius_free", "Synty_standard",
                    "Scenario_owned", "unknown".
    cost_usd        Approximate USD cost of generating this asset. 0.0 for
                    free sources, ~0.02-0.05 for cloud 2D, ~0.10-1.00 for 3D.
                    Used by cache eviction to prefer keeping expensive assets.
    generated_at    ISO-8601 UTC timestamp of when the asset was created.
                    Auto-populated for new entries.
    prompt          For ai_* sources, the prompt used to generate the asset.
                    Stored verbatim so a regenerate-with-newer-model knows
                    exactly what to ask for. None for non-AI sources.
    swap_safe       True if a future generator run is safe to overwrite this
                    file (procedural, AI cache hits). False for hand-curated
                    or hand-imported assets that should never be auto-replaced.
                    Default: True. Set to False for explicit imports/edits.
    generator_version  Bump this string when the generator/model behind a
                    source changes. Used to invalidate AI cache after model
                    upgrades without dropping pack/procedural entries.
                    Default: "v1".
    redistribution  True if the asset can be committed to public git or
                    bundled in distributable artifacts. CC0/MIT free packs:
                    True. Synty paid: False (license forbids redistribution).
                    Default: True (since most assets are safe by default).

Backwards compatibility:
  `make_manifest` still accepts the old minimal arg list (asset_id, kind,
  path, **extra) to keep existing callers working. New optional kwargs
  (source, pack_name, etc.) default to safe values, so legacy callers
  produce manifests with source="unknown", license="unknown", etc.

  `migrate_manifest` upgrades a legacy dict in-place to the new schema by
  inferring missing fields from the path (e.g. path under packs/synty/
  → source=pack, pack_name=synty, redistribution=False) and writing safe
  defaults for everything else. Idempotent — calling on an already-new
  manifest is a no-op.
"""

from __future__ import annotations

from datetime import UTC, datetime
from typing import Any

# ─── Defaults ────────────────────────────────────────────────────────────

_DEFAULT_SOURCE = "unknown"
_DEFAULT_LICENSE = "unknown"
_DEFAULT_COST_USD = 0.0
_DEFAULT_SWAP_SAFE = True
_DEFAULT_GENERATOR_VERSION = "v1"
_DEFAULT_REDISTRIBUTION = True


def _now_iso() -> str:
    return datetime.now(UTC).isoformat(timespec="seconds")


def make_manifest(
    asset_id: str,
    kind: str,
    path: str,
    *,
    biome: str | None = None,
    tags: list[str] | None = None,
    source: str = _DEFAULT_SOURCE,
    pack_name: str | None = None,
    license: str = _DEFAULT_LICENSE,
    cost_usd: float = _DEFAULT_COST_USD,
    generated_at: str | None = None,
    prompt: str | None = None,
    swap_safe: bool = _DEFAULT_SWAP_SAFE,
    generator_version: str = _DEFAULT_GENERATOR_VERSION,
    redistribution: bool = _DEFAULT_REDISTRIBUTION,
    **extra: Any,
) -> dict[str, Any]:
    """Build a fully-populated manifest with the new schema fields.

    Backwards compatible: legacy callers passing only (asset_id, kind, path)
    plus a couple of **extra fields still work; the unspecified provenance
    fields take their safe defaults.

    Extra kwargs go into the manifest verbatim so callers can attach
    domain-specific metadata without a schema change.
    """
    manifest: dict[str, Any] = {
        "schema_version": "1.0.0",
        "asset_id": asset_id,
        "kind": kind,
        "path": path,
        "source": source,
        "license": license,
        "cost_usd": cost_usd,
        "generated_at": generated_at or _now_iso(),
        "swap_safe": swap_safe,
        "generator_version": generator_version,
        "redistribution": redistribution,
    }
    if biome is not None:
        manifest["biome"] = biome
    if tags is not None:
        manifest["tags"] = list(tags)
    if pack_name is not None:
        manifest["pack_name"] = pack_name
    if prompt is not None:
        manifest["prompt"] = prompt
    manifest.update(extra)
    return manifest


def migrate_manifest(manifest: dict[str, Any]) -> dict[str, Any]:
    """Upgrade a legacy or partial manifest to the new schema in-place.

    Inferences from the path:
      - path under .shared/baked/packs/synty/   → source=pack, license=Synty_standard,
                                                   pack_name="synty", redistribution=False
      - path under .shared/baked/packs/kaykit/  → source=pack, license=KayKit_free,
                                                   pack_name="kaykit"
      - path under .shared/baked/packs/kenney/  → source=pack, license=CC0,
                                                   pack_name="kenney"
      - path under .shared/baked/packs/quaternius/ → source=pack, license=Quaternius_free,
                                                      pack_name="quaternius"
      - path under any other .shared/baked/<kind>/ subdirectory → source=procedural,
                                                                   license=CC0
                                                                   (the seed pipeline)
      - anything else (e.g. legacy pytest temp paths)            → source=unknown

    Existing fields are NEVER overwritten — migration only fills in MISSING
    fields. So a manifest that already has source=ai_3d won't get clobbered
    even if its path looks like a pack.

    Idempotent: calling on a fully-populated new-schema manifest is a no-op.
    Returns the same dict it was given (mutated). Callers that need a copy
    should dict() it first.
    """
    # Infer provenance from path if source is missing
    if "source" not in manifest:
        path_str = str(manifest.get("path", "")).replace("\\", "/").lower()
        inferred_source = _DEFAULT_SOURCE
        inferred_pack = None
        inferred_license = _DEFAULT_LICENSE
        inferred_redistribution = _DEFAULT_REDISTRIBUTION

        if "/baked/packs/synty/" in path_str or "/packs/synty/" in path_str:
            inferred_source = "pack"
            inferred_pack = "synty"
            inferred_license = "Synty_standard"
            inferred_redistribution = False
        elif "/baked/packs/kaykit/" in path_str or "/packs/kaykit/" in path_str:
            inferred_source = "pack"
            inferred_pack = "kaykit"
            inferred_license = "KayKit_free"
        elif "/baked/packs/kenney/" in path_str or "/packs/kenney/" in path_str:
            inferred_source = "pack"
            inferred_pack = "kenney"
            inferred_license = "CC0"
        elif "/baked/packs/quaternius/" in path_str or "/packs/quaternius/" in path_str:
            inferred_source = "pack"
            inferred_pack = "quaternius"
            inferred_license = "Quaternius_free"
        elif "/baked/" in path_str and (
            "/creature_token/" in path_str
            or "/item_icon/" in path_str
            or "/terrain/" in path_str
            or "/tileset/" in path_str
        ):
            # These are the seed pipeline outputs — procedural Pillow art
            inferred_source = "procedural"
            inferred_license = "CC0"
        # else: leave as unknown — likely legacy pytest temp path or hand-imported

        manifest["source"] = inferred_source
        if inferred_pack and "pack_name" not in manifest:
            manifest["pack_name"] = inferred_pack
        if "license" not in manifest:
            manifest["license"] = inferred_license
        if "redistribution" not in manifest:
            manifest["redistribution"] = inferred_redistribution

    # Fill remaining fields with safe defaults — only if missing
    manifest.setdefault("schema_version", "1.0.0")
    manifest.setdefault("license", _DEFAULT_LICENSE)
    manifest.setdefault("cost_usd", _DEFAULT_COST_USD)
    manifest.setdefault("generated_at", _now_iso())
    manifest.setdefault("swap_safe", _DEFAULT_SWAP_SAFE)
    manifest.setdefault("generator_version", _DEFAULT_GENERATOR_VERSION)
    manifest.setdefault("redistribution", _DEFAULT_REDISTRIBUTION)

    return manifest


def is_modern_manifest(manifest: dict[str, Any]) -> bool:
    """Check if a manifest already has the new-schema provenance fields.

    Used by `migrate_manifest` callers that want to avoid the work of
    walking a fully-migrated catalog. Pure read; no side effects.
    """
    return "source" in manifest and "license" in manifest
